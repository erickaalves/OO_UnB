package modelo;

public class Item {
	private String nomeItem;
	private Double quantidade;
	private String unidMedida;
	private Double valor;
	
	public String getNomeItem() {
		return nomeItem;
	}
	public String getQuantidade() {
		return quantidade;
	}
	public String getUnidMedida() {
		return unidMedida;
	}
	public String getValor() {
		return valor;
	}
}