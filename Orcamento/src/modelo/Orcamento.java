package modelo;

public class Orcamento {
	private String tituloOrc;
	private String relatInicial;
	private String descAtividades;	
	
	public String getTitulo() {
		return tituloOrc;
	}
	public String getRelatorio() {
		return relatInicial;
	}
	public String getDescricao() {
		return descAtividades;	
	}
		
}